    private void jListProjectListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListProjectListMouseClicked
        if (jListProjectList.getModel().getSize() > 0) {
            try {
                int projectIndex = jListProjectList.getSelectedIndex();
                Project project = (Project) projectListModel.get(projectIndex);

                loadTasks(project.getId());
            } catch (SQLException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jListProjectListMouseClicked

    private void jLabelAddTaskMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelAddTaskMouseClicked
        if (!projectListModel.isEmpty()) {
            TaskDialogScreen taskDialogScreen = new TaskDialogScreen(this, true);

            int projectIndex = jListProjectList.getSelectedIndex();
            Project project = projectListModel.getElementAt(projectIndex);

            taskDialogScreen.setProject(project);
            taskDialogScreen.setVisible(true);

            taskDialogScreen.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosed(WindowEvent event) {
                    try {
                        loadTasks(project.getId());
                    } catch (SQLException ex) {
                        Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
        }
    }//GEN-LAST:event_jLabelAddTaskMouseClicked

    private void jTableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTasksMouseClicked
        int rowIndex = jTableTasks.rowAtPoint(evt.getPoint());
        int columnIndex = jTableTasks.columnAtPoint(evt.getPoint());
        Task task = taskTableModel.getTasks().get(rowIndex);

        // Pegar o clique apenas no icone                
        if (jTableTasks.getColumnClass(columnIndex).equals(String.class)) {
            Component cellRendererComponent = jTableTasks.getCellRenderer(rowIndex, columnIndex).
                    getTableCellRendererComponent(
                            jTableTasks, task, rootPaneCheckingEnabled, rootPaneCheckingEnabled, rowIndex, columnIndex);

            Rectangle cellRect = jTableTasks.getCellRect(rowIndex, columnIndex, true);
            cellRendererComponent.setLocation((int) cellRect.getCenterX() - 16, 10);

            if (cellRendererComponent.getBounds().contains(evt.getPoint())) {
                int projectIndex = jListProjectList.getSelectedIndex();
                Project project = projectListModel.getElementAt(projectIndex);

                switch (columnIndex) {
                    case 4:
                        TaskDialogScreen taskDialogScreen = new TaskDialogScreen(this, true);

                        taskDialogScreen.setProject(project);
                        taskDialogScreen.setTask(task);
                        taskDialogScreen.setVisible(true);

                        taskDialogScreen.addWindowListener(new WindowAdapter() {
                            @Override
                            public void windowClosed(WindowEvent event) {
                                try {
                                    loadTasks(project.getId());
                                } catch (SQLException ex) {
                                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                                }
                            }
                        });

                        break;
                    case 5:
                        int response = JOptionPane.showConfirmDialog(jTableTasks, "Deseja realmente excluir a tarefa?",
                                "Excluir Tarefa", 0);

                        if (response == 0) {
                            try {
                                taskController.removeById(task.getId());
                            } catch (SQLException ex) {
                                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                            }

                            taskTableModel.getTasks().remove(task);

                            try {
                                loadTasks(project.getId());
                            } catch (SQLException ex) {
                                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }

                        break;
                }
            }
        } else if (columnIndex == 3) {
            try {
                taskController.update(task);
            } catch (SQLException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jTableTasksMouseClicked


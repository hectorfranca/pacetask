    public void initDataControllers() {
        projectController = new ProjectController();
        taskController = new TaskController();
    }

    public void initComponentsModel() throws SQLException {
        projectListModel = new DefaultListModel<Project>();
        taskTableModel = new TaskTableModel();

        jListProjectList.setModel(projectListModel);
        jTableTasks.setModel(taskTableModel);

        loadProjects();

        if (!projectListModel.isEmpty()) {
            jListProjectList.setSelectedIndex(0);

            Project project = projectListModel.get(0);

            loadTasks(project.getId());
        }
    }

    public void loadProjects() throws SQLException {
        List<Project> projectsList = projectController.getAll();

        projectListModel.clear();

        for (Project project : projectsList) {
            projectListModel.addElement(project);
        }
    }

    public void loadTasks(int projectId) throws SQLException {
        List<Task> tasks = taskController.getAll(projectId);

        taskTableModel.setTasks(tasks);

        showTableTasks(!tasks.isEmpty());
    }

